#!/usr/bin/env node
// Propagate a release version to every versioned manifest in the repo.
//
// Usage:
//   node scripts/sync-versions.mjs <version>            # write mode
//   node scripts/sync-versions.mjs <version> --check    # verify only, exit 1 on drift
//
// <version> accepts either "0.2.0" or "v0.2.0" (leading v is stripped).
//
// Files covered:
//   - Cargo.toml                           [workspace.package].version
//   - crates/lora-node/package.json        .version
//   - crates/lora-wasm/package.json        .version
//   - apps/loradb.com/package.json         .version
//   - crates/lora-python/pyproject.toml    [project].version
//
// Excluded on purpose:
//   - Root package.json                 dev-tooling only, stays 0.0.0
//   - crates/lora-wasm/pkg-*/package.json  generated by wasm-pack, gitignored
//   - Cargo.lock, package-lock.json     refreshed separately via `cargo check --workspace`
//                                       and `npm install --package-lock-only` in each dir
//
// Lockfile refresh is intentionally NOT done here — keeping this script pure
// means it works in CI `--check` mode without network access or a Rust toolchain.
// Callers (release workflow, contributors) run the lockfile commands afterwards.

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, '..');

const args = process.argv.slice(2);
if (args.length === 0) {
  console.error('usage: sync-versions.mjs <version> [--check]');
  process.exit(2);
}

const rawVersion = args[0];
const checkOnly = args.includes('--check');
const version = rawVersion.startsWith('v') ? rawVersion.slice(1) : rawVersion;

if (!/^\d+\.\d+\.\d+(-[0-9A-Za-z.-]+)?$/.test(version)) {
  console.error(`invalid version: ${rawVersion}`);
  console.error('expected semver like 0.2.0 or 0.2.0-rc.1 (optionally prefixed with v)');
  process.exit(2);
}

/** @type {Array<{path: string, kind: 'cargo-workspace' | 'package-json' | 'pyproject'}>} */
const targets = [
  { path: 'Cargo.toml', kind: 'cargo-workspace' },
  { path: 'crates/lora-node/package.json', kind: 'package-json' },
  { path: 'crates/lora-wasm/package.json', kind: 'package-json' },
  { path: 'apps/loradb.com/package.json', kind: 'package-json' },
  { path: 'crates/lora-python/pyproject.toml', kind: 'pyproject' },
];

let changed = 0;
let drift = 0;
const drifts = [];

for (const target of targets) {
  const absolute = resolve(ROOT, target.path);
  const original = readFileSync(absolute, 'utf8');
  const { updated, currentVersion } = applyVersion(original, target.kind, version);

  if (currentVersion === null) {
    console.error(`could not locate version in ${target.path}`);
    process.exit(1);
  }

  if (currentVersion === version) {
    console.log(`ok       ${target.path} already at ${version}`);
  } else if (checkOnly) {
    drift += 1;
    drifts.push(`  ${target.path}: has ${currentVersion}, expected ${version}`);
  } else {
    writeFileSync(absolute, updated);
    console.log(`bumped   ${target.path}: ${currentVersion} -> ${version}`);
    changed += 1;
  }

  // Cargo workspace has a second version surface to keep in lockstep:
  // the pinned internal crate deps in [workspace.dependencies]. These use
  // `version = "=X.Y.Z"` so a bump of the workspace version must rewrite
  // each of them, otherwise `cargo publish` will fail on the first crate
  // whose dep version drifted.
  if (target.kind === 'cargo-workspace') {
    const updatedAfterDeps = applyCargoWorkspaceInternalDeps(
      checkOnly ? original : readFileSync(absolute, 'utf8'),
      version,
    );
    for (const dep of updatedAfterDeps.drifts) {
      if (checkOnly) {
        drift += 1;
        drifts.push(`  ${target.path} [workspace.dependencies.${dep.name}]: has ${dep.currentVersion}, expected =${version}`);
      }
    }
    if (!checkOnly && updatedAfterDeps.changed > 0) {
      writeFileSync(absolute, updatedAfterDeps.source);
      console.log(`bumped   ${target.path} [workspace.dependencies]: ${updatedAfterDeps.changed} dep pin(s) updated to =${version}`);
      changed += 1;
    }
  }
}

if (checkOnly && drift > 0) {
  console.error(`version drift detected for ${version}:`);
  for (const line of drifts) console.error(line);
  process.exit(1);
}

if (checkOnly) {
  console.log(`all versions match ${version}`);
} else if (changed > 0) {
  console.log('');
  console.log(`updated ${changed} file(s). next:`);
  console.log('  cargo check --workspace              # refresh Cargo.lock');
  console.log('  (cd crates/lora-node    && npm install --package-lock-only --ignore-scripts)');
  console.log('  (cd crates/lora-wasm    && npm install --package-lock-only --ignore-scripts)');
  console.log('  (cd apps/loradb.com     && npm install --package-lock-only --ignore-scripts)');
  console.log('  git commit -am "chore(release): v' + version + '"');
} else {
  console.log(`all versions already at ${version}`);
}

/**
 * @param {string} source
 * @param {'cargo-workspace' | 'package-json' | 'pyproject'} kind
 * @param {string} next
 * @returns {{ updated: string, currentVersion: string | null }}
 */
function applyVersion(source, kind, next) {
  if (kind === 'cargo-workspace') {
    return applyInSection(source, 'workspace.package', next);
  }
  if (kind === 'pyproject') {
    return applyInSection(source, 'project', next);
  }
  return applyPackageJson(source, next);
}

/**
 * Updates `version = "..."` inside a specific TOML `[section]`, leaving every
 * other `version = "..."` line untouched (e.g. `[workspace.dependencies]`).
 *
 * @param {string} source
 * @param {string} section
 * @param {string} next
 */
function applyInSection(source, section, next) {
  const lines = source.split('\n');
  let inSection = false;
  let current = null;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const sectionMatch = line.match(/^\s*\[([^\]]+)\]\s*$/);
    if (sectionMatch) {
      inSection = sectionMatch[1].trim() === section;
      continue;
    }
    if (!inSection) continue;

    const versionMatch = line.match(/^(\s*version\s*=\s*)"([^"]+)"(.*)$/);
    if (versionMatch) {
      current = versionMatch[2];
      lines[i] = `${versionMatch[1]}"${next}"${versionMatch[3]}`;
      break;
    }
  }

  return { updated: lines.join('\n'), currentVersion: current };
}

/**
 * Updates the top-level `"version"` field in a package.json using plain
 * string rewriting to preserve formatting / trailing newlines / key order.
 *
 * @param {string} source
 * @param {string} next
 */
function applyPackageJson(source, next) {
  const match = source.match(/("version"\s*:\s*)"([^"]+)"/);
  if (!match) return { updated: source, currentVersion: null };
  const current = match[2];
  const updated = source.replace(match[0], `${match[1]}"${next}"`);
  return { updated, currentVersion: current };
}

/**
 * Rewrites every `<crate> = { ..., version = "=...", ... }` inside
 * `[workspace.dependencies]` so their pin matches the workspace version.
 *
 * Only lines whose crate name starts with `lora-` are touched — external
 * deps are untouched.
 *
 * @param {string} source
 * @param {string} next
 * @returns {{ source: string, changed: number, drifts: Array<{name: string, currentVersion: string}> }}
 */
function applyCargoWorkspaceInternalDeps(source, next) {
  const lines = source.split('\n');
  let inSection = false;
  let changed = 0;
  const drifts = [];

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const sectionMatch = line.match(/^\s*\[([^\]]+)\]\s*$/);
    if (sectionMatch) {
      inSection = sectionMatch[1].trim() === 'workspace.dependencies';
      continue;
    }
    if (!inSection) continue;

    // Match e.g. `lora-ast = { path = "crates/lora-ast", version = "=0.1.0" }`.
    const depMatch = line.match(/^(\s*)(lora-[A-Za-z0-9_-]+)\s*=\s*\{(.*)\}\s*$/);
    if (!depMatch) continue;
    const [full, indent, name, inner] = depMatch;
    const versionMatch = inner.match(/(^|[,{\s])version\s*=\s*"(=?)([^"]+)"/);
    if (!versionMatch) continue;
    const currentPrefix = versionMatch[2]; // `=` or empty
    const currentVersion = versionMatch[3];
    const expected = `=${next}`;
    const currentLiteral = `${currentPrefix}${currentVersion}`;
    if (currentLiteral === expected) continue;
    drifts.push({ name, currentVersion: currentLiteral });
    const rewrittenInner = inner.replace(
      /(^|[,{\s])version\s*=\s*"=?[^"]+"/,
      (match, leading) => `${leading}version = "${expected}"`,
    );
    lines[i] = `${indent}${name} = {${rewrittenInner}}`;
    changed += 1;
  }

  return { source: lines.join('\n'), changed, drifts };
}
